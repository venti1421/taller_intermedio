# Explorando range()
# Escribe un programa que cree y muestre las listas resultantes de:

# list(range(5))

# list(range(1, 6))

# list(range(0, 10, 2))

print("Esta es la primera lista:")
for list_1 in range(5):
    print(list_1)

print("Esta es la segunda lista:")
for list_2 in range(1,6):
    print(list_2)

print("Esta es la tercera lista:")
for list_3 in range(0,10,2):
    print(list_3)